#Лемма о безопасном ребре говорит, что мы можем
#строить минимальный остов постепенно, добавляя по одному ребра,
# про которые мы точно знаем, что они минимальные для соединения какого-то разреза.
#Один из подходов это использовать заключается в алгоритме Прима:
#Изначально остов — одна произвольная вершина.
#Пока минимальный остов не найден, выбирается ребро минимального веса,
# исходящее из какой-нибудь вершины текущего остова в вершину, которую мы ещё не добавили.
# Добавляем это ребро в остов и начинаем заново, пока остов не будет найден.
#Этот алгоритм очень похож на алгоритм Дейкстры, только мы выбираем следующую вершину
# с другой весовой функцией — вес минимального соединяющего ребра вместо суммарного расстояния до неё.



import heapq

def prim(graph, n):
    """
    Алгоритм Прима для MST
    graph: [[(v, w), ...], ...] - списки смежности
    """
    INF = float('inf')
    min_edge = [INF] * n          # min вес до дерева
    used = [False] * n            # в MST?
    parent = [-1] * n             # для восстановления пути
    min_edge[0] = 0               # старт с вершины 0

    pq = [(0, 0)]                 # (вес, вершина)

    mst_weight = 0

    while pq:
        weight, u = heapq.heappop(pq)
        if used[u] or weight > min_edge[u]:
            continue

        used[u] = True
        mst_weight += weight

        for v, w in graph[u]:
            if not used[v] and w < min_edge[v]:
                min_edge[v] = w
                parent[v] = u
                heapq.heappush(pq, (w, v))

    return mst_weight, parent

# Тестовый граф
graph = [
    [(2,1), (1,3)],      # 0
    [(0,3), (3,5)],      # 1
    [(0,1), (4,2), (5,3)], # 2
    [(1,5)],             # 3
    [(2,2), (6,1)],      # 4
    [(2,3)],             # 5
    [(4,1)]              # 6
]

weight, tree = prim(graph, 7)
print(f"MST вес: {weight}")  # 15
print("Дерево:", tree)       # [-1, 0, 0, 1, 2, 2, 4]