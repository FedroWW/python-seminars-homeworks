def dinic(G, s, t):
    """
    Алгоритм Диница для поиска максимального потока в ориентированном графе.

    Параметры:
    G - граф в виде словаря смежности: {вершина: {сосед: пропускная_способность}}
    s - исходная вершина (источник)
    t - конечная вершина (сток)

    Возвращает:
    max_flow - величина максимального потока
    """

    # ==================== ФУНКЦИЯ BFS ДЛЯ ПОСТРОЕНИЯ СЛОИСТОЙ СЕТИ ====================
    def bfs(residual_graph, level):
        """
        Обход в ширину (BFS) для построения слоистой сети.

        Принцип работы:
        - Присваиваем каждой вершине уровень (расстояние от источника)
        - Используем только рёбра с положительной остаточной пропускной способностью
        - Строим уровневую структуру: вершины на уровне i соединены с вершинами на уровне i+1

        Параметры:
        residual_graph - остаточный граф (текущие пропускные способности)
        level - словарь для хранения уровней вершин (инициализируется -1)

        Возвращает:
        True - если сток достижим из источника, False - в противном случае
        """
        queue = [s]  # Очередь для BFS, начинаем с источника
        level[s] = 0  # Источнику присваиваем уровень 0

        while queue:
            u = queue.pop(0)  # Извлекаем вершину из начала очереди (неэффективно, лучше использовать collections.deque)
            # Перебираем всех соседей вершины u
            for v, capacity in residual_graph[u].items():
                # Если вершина v ещё не посещена (level = -1)
                # и ребро имеет положительную остаточную пропускную способность
                if level[v] == -1 and capacity > 0:
                    # Присваиваем v уровень на 1 больше, чем у u
                    level[v] = level[u] + 1
                    queue.append(v)  # Добавляем v в очередь для дальнейшего обхода

        # Возвращаем True, если сток t достижим (имеет уровень не -1)
        return level[t] != -1

    # ==================== ФУНКЦИЯ DFS ДЛЯ ПОИСКА БЛОКИРУЮЩЕГО ПОТОКА ====================
    def dfs(residual_graph, u, flow, level, ptr):
        """
        Поиск в глубину (DFS) для нахождения блокирующего потока в слоистой сети.

        Принцип работы:
        - Двигаемся по рёбрам, которые ведут на следующий уровень (level[v] == level[u] + 1)
        - Ищем увеличивающий путь от источника к стоку
        - Отправляем поток, ограниченный минимальной пропускной способностью на пути
        - Обновляем остаточные пропускные способности

        Параметры:
        residual_graph - остаточный граф
        u - текущая вершина
        flow - текущий поток, который можно отправить (минимальная пропускная способность на пути)
        level - уровни вершин (из BFS)
        ptr - указатели для оптимизации (перебор соседей продолжается с последнего места)

        Возвращает:
        pushed - величина отправленного потока (0, если путь не найден)
        """
        # Базовый случай: достигли стока
        if u == t:
            return flow

        # Перебираем соседей, начиная с последнего обработанного (оптимизация)
        # ptr[u] хранит индекс следующего соседа для проверки
        while ptr[u] < len(residual_graph[u]):
            # Получаем соседа v и пропускную способность ребра u->v
            # list() создаёт список из items() для доступа по индексу
            v, capacity = list(residual_graph[u].items())[ptr[u]]

            # Проверяем два условия для движения по слоистой сети:
            # 1) v находится на следующем уровне (level[u] + 1)
            # 2) ребро имеет положительную остаточную пропускную способность
            if level[v] == level[u] + 1 and capacity > 0:
                # Вычисляем максимальный поток, который можно отправить по текущему пути
                # Это минимум из: уже накопленного потока и пропускной способности ребра
                min_capacity = min(flow, capacity)

                # Рекурсивно ищем путь дальше, от вершины v
                pushed = dfs(residual_graph, v, min_capacity, level, ptr)

                # Если нашли увеличивающий путь (pushed > 0)
                if pushed > 0:
                    # Уменьшаем пропускную способность прямого ребра
                    residual_graph[u][v] -= pushed
                    # Увеличиваем пропускную способность обратного ребра
                    # get(u, 0) - если обратного ребра нет, создаём его с 0
                    residual_graph[v][u] = residual_graph[v].get(u, 0) + pushed
                    return pushed  # Возвращаем найденный поток

            # Переходим к следующему соседу
            ptr[u] += 1

        # Если ни один путь не найден, возвращаем 0
        return 0

    # ==================== ОСНОВНАЯ ЧАСТЬ АЛГОРИТМА ====================

    # Шаг 1: Инициализация остаточного графа
    # Копируем исходный граф, преобразуя его в словарь словарей
    residual_graph = {
        u: {v: capacity for v, capacity in neighbors.items()}
        for u, neighbors in G.items()
    }

    max_flow = 0  # Переменная для хранения максимального потока

    # Инициализируем уровни вершин (-1 означает "не посещена")
    level = {node: -1 for node in G}

    # Шаг 2: Основной цикл алгоритма
    # Пока существует путь от источника к стоку в остаточном графе
    while bfs(residual_graph, level):
        # ptr (указатели) для оптимизации DFS
        # Каждая вершина хранит индекс следующего соседа для проверки
        # Это позволяет не перебирать уже "бесполезные" рёбра повторно
        ptr = {node: 0 for node in G}

        # Шаг 3: Находим и отправляем блокирующий поток
        # Запускаем DFS для поиска увеличивающего пути
        flow = dfs(residual_graph, s, float('Inf'), level, ptr)

        # Продолжаем искать увеличивающие пути в текущей слоистой сети
        # пока они существуют (flow > 0)
        while flow > 0:
            max_flow += flow  # Добавляем найденный поток к общему
            flow = dfs(residual_graph, s, float('Inf'), level, ptr)

        # Шаг 4: Сбрасываем уровни для построения новой слоистой сети
        # (на следующей итерации цикла while)
        level = {node: -1 for node in G}

    # Шаг 5: Возвращаем величину максимального потока
    return max_flow


# ==================== ПРИМЕР ИСПОЛЬЗОВАНИЯ ====================
"""
Пример графа:
    Исходная сеть:

        10        5
    0 ----> 1 ----> 3
     \      ^
      \    / 15
       \  /
        > 2
        15

    Представление в виде словаря:
    G = {
        0: {1: 10, 2: 15},
        1: {3: 5},
        2: {1: 15, 3: 10},
        3: {}
    }

    Запуск: dinic(G, 0, 3)

    Максимальный поток = 15 (10 по пути 0->1->3 и 5 по пути 0->2->1->3)
"""